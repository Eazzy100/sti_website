            <li class="list-item">
                <strong>Urethral Discharge Syndrome (Gonorrhea, Chlamydia, Trichomoniasis)</strong><br>
                - Urine Test: A sample of urine is tested for bacterial infections.<br>
                - Urethral Swab: A sample is collected from the urethra and tested for bacteria.<br>
                - Nucleic Acid Amplification Test (NAAT): Detects bacterial DNA, highly accurate for gonorrhea and chlamydia.
            </li>
            <li class="list-item">
                <strong>Vaginal Discharge Syndrome (Chlamydia, Gonorrhea, Trichomoniasis, Bacterial Vaginosis)</strong><br>
                - Pelvic Examination: A doctor examines the vagina and cervix for discharge or inflammation.<br>
                - Vaginal/Cervical Swab: A sample is collected and tested for bacterial infections.<br>
                - NAAT Test: Detects chlamydia and gonorrhea DNA in vaginal secretions or urine.<br>
                - Wet Mount Test: A microscope is used to check for trichomoniasis in vaginal fluid.
            </li>
            <li class="list-item">
                <strong>Genital Warts (HPV)</strong><br>
                - Visual Inspection: A doctor examines warts in the genital or anal area.<br>
                - HPV DNA Test: Identifies high-risk HPV strains that may lead to cervical cancer.<br>
                - Pap Smear (For Women): Checks for abnormal cervical cells caused by HPV.
            </li>
            <li class="list-item">
                <strong>Genital Ulcers Syndrome (Syphilis, Herpes, Chancroid)</strong><br>
                - Blood Test (Syphilis):<br>
                Rapid Plasma Reagin (RPR) Test or Venereal Disease Research Laboratory (VDRL) Test detects syphilis antibodies.<br>
                Treponemal Test (TPHA/FTA-ABS): Confirms syphilis diagnosis.<br>
                - Herpes Test (HSV-1 & HSV-2):<br>
                Swab of the Sore: Fluid from a sore is tested for the herpes virus.<br>
                - Blood Test: Detects herpes antibodies in the bloodstream.
            </li>
            <li class="list-item">
                <strong>HIV (Human Immunodeficiency Virus)</strong><br>
                - Rapid HIV Test: Uses a blood sample or oral swab to detect HIV antibodies (results in 20-30 minutes).<br>
                - ELISA (Enzyme-Linked Immunosorbent Assay): A laboratory test that detects HIV antibodies.<br>
                - Western Blot Test: Confirms HIV diagnosis if ELISA is positive.<br>
                - PCR (Polymerase Chain Reaction) Test: Detects HIV RNA in the blood, used for early detection.
            </li>
            <li class="list-item">
                <strong>Syphilis</strong><br>
                - Blood Test: Detects syphilis antibodies (RPR, VDRL, TPHA).<br>
                - Dark-Field Microscopy: Examines syphilis bacteria from a sore sample.<br>
                - Lumbar Puncture (For Neurosyphilis): Tests cerebrospinal fluid if syphilis affects the brain.
            </li>
            <li class="list-item">
                <strong>Herpes (HSV-1 & HSV-2)</strong><br>
                - Swab Test: Fluid from a blister or sore is tested for herpes virus.<br>
                - Blood Test: Detects antibodies against HSV-1 and HSV-2.<br>
                - PCR Test: Identifies herpes DNA in blood or sore samples.
            </li>
        </ul>
