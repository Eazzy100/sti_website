        <h2 class="section-title">TREATMENT</h2>
        <p class="section-text">Below are different treatments done for the different STI’s</p>
        <ul class="overview-list">
            <li class="list-item">
                <strong>Urethral Discharge Syndrome (Gonorrhea, Chlamydia, Trichomoniasis)</strong><br>
                - Gonorrhea<br>
                Antibiotics: Ceftriaxone (injection) + Azithromycin (oral) or Doxycycline (oral)<br>
                Partner Treatment: Sexual partners should also be treated to prevent reinfection.<br>
                - Chlamydia<br>
                Antibiotics: Azithromycin (single dose) or Doxycycline (7-day course)<br>
                Avoid sex until treatment is completed and symptoms disappear.<br>
                - Trichomoniasis<br>
                Antibiotic: Metronidazole (oral, single dose or 7-day course)<br>
                Alcohol Warning: Avoid alcohol during treatment as it can cause severe side effects.
            </li>
            <li class="list-item">
                <strong>Vaginal Discharge Syndrome (Chlamydia, Gonorrhea, Trichomoniasis, Bacterial Vaginosis)</strong><br>
                - Bacterial Vaginosis (BV)<br>
                Antibiotic: Metronidazole (oral or vaginal gel)<br>
                Prevention: Avoid douching and scented vaginal products.<br>
                - Candidiasis (Yeast Infection)<br>
                Antifungal Creams: Clotrimazole, Miconazole, or Fluconazole (oral tablet)<br>
                Maintain good vaginal hygiene and avoid excessive moisture.
            </li>
            <li class="list-item">
                <strong>Genital Warts (HPV)</strong><br>
                No Cure for HPV, but Warts Can Be Treated:<br>
                Topical Treatments: Imiquimod, Podophyllin, or Trichloroacetic Acid (TCA)<br>
                Cryotherapy: Freezing warts with liquid nitrogen.<br>
                Surgical Removal: Large warts can be removed by laser, electrocautery, or excision.<br>
                HPV Vaccine: Gardasil 9 prevents high-risk HPV types that cause warts and cervical cancer.
            </li>
            <li class="list-item">
                <strong>Genital Ulcer Syndrome (Syphilis, Herpes, Chancroid)</strong><br>
                - Syphilis<br>
                Early Stage: Benzathine Penicillin G (single injection)
                Late Stage: Multiple Penicillin injections over several weeks.
Allergy Alternative: Doxycycline or Ceftriaxone (for those allergic to penicillin).
Herpes (HSV-1 & HSV-2)
No Cure, but Symptoms Can Be Managed:
Antiviral Drugs: Acyclovir, Valacyclovir, or Famciclovir reduce outbreak severity and frequency.
Home Care: Keep the area clean and dry, avoid sexual contact during outbreaks.
Chancroid (Haemophilus ducreyi infection)
Antibiotics: Azithromycin, Ceftriaxone, or Ciprofloxacin
Pain Relief: Warm saltwater soaks and pain relievers help with ulcer healing.
5. HIV (Human Immunodeficiency Virus)
No Cure, but Managed with Antiretroviral Therapy (ART):
First-line ART: Tenofovir + Lamivudine + Dolutegravir (TLD) combination.
Prevention: Pre-Exposure Prophylaxis (PrEP) for high-risk individuals.
Post-Exposure Prophylaxis (PEP): A 28-day treatment after possible HIV exposure.
6. Herpes (HSV-1 & HSV-2)
No Cure, but Outbreaks Can Be Controlled:
Antivirals: Acyclovir, Valacyclovir, Famciclovir reduce outbreaks.<br>
Suppressive Therapy: Daily antiviral medication can prevent frequent recurrences.
